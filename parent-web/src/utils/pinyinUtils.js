import { pinyin } from 'pinyin-pro';

/**
 * 为文本添加拼音标注
 * @param {string} text - 需要标注拼音的文本
 * @param {object} options - 配置选项
 * @returns {string} 带拼音的HTML
 */
export const addPinyin = (text, options = {}) => {
  if (!text) return '';
  
  const {
    type = 'html', // 'html' | 'array'
    toneType = 'symbol', // 'symbol' | 'num' | 'none'
    filterCommon = true, // 是否过滤常见字
  } = options;

  // 如果是英文或数字,直接返回
  if (/^[a-zA-Z0-9\s]+$/.test(text)) {
    return text;
  }

  try {
    const result = pinyin(text, {
      type: 'array',
      toneType,
      nonZh: 'consecutive',
    });

    if (type === 'array') {
      return result;
    }

    // 生成HTML格式: <ruby>汉<rt>hàn</rt></ruby>
    const chars = text.split('');
    let html = '';
    
    for (let i = 0; i < chars.length; i++) {
      const char = chars[i];
      const py = result[i] || '';
      
      // 判断是否是中文
      if (/[\u4e00-\u9fa5]/.test(char)) {
        // 如果开启过滤常见字,且是常见字,则不显示拼音
        if (filterCommon && isCommonChar(char)) {
          html += char;
        } else {
          html += `<ruby>${char}<rt>${py}</rt></ruby>`;
        }
      } else {
        html += char;
      }
    }

    return html;
  } catch (error) {
    console.error('拼音标注失败:', error);
    return text;
  }
};

