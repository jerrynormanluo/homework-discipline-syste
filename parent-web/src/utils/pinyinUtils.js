/**
 * 批量为作业内容添加拼音
 * @param {Array} homeworks - 作业列表
 * @returns {Array} 带拼音的作业列表
 */
export const addPinyinToHomeworks = (homeworks, options = {}) => {
  if (!Array.isArray(homeworks)) return homeworks;
  
  return homeworks.map(hw => ({
    ...hw,
    title_pinyin: hw.title ? addPinyin(hw.title, options) : '',
    content_pinyin: hw.content ? addPinyin(hw.content, options) : '',
  }));
};
